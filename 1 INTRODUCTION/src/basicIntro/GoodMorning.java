package basicIntro;

public class GoodMorning 					//CLASS BODY
{											// public IS ACCESS MODIFIER. IT CAN ACCESS THIS CLASS.
	
	public static void main (String[]args) 		//METHOD BODY
	{								// public IS ACCESS MODIFIER.IT CAN ACCESS THIS METHOD.
									// static IS PREDEFINED KEYWORD WHICH IS USED TO RUN PROGRAMME WITHOUT OBJECT.
		
		System.out.println("VERY GOOD MORNING");  
		
		// THIS IS ONE & ONLY PRINTING STATEMENT IN JAVA.
		
		//System ===> predefined utility class
		//.out =====> object belongs to System class.
		//Println() ====> utility method which is used to send any string to console which is given in method sign..
	}
	
}
